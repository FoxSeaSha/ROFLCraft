package bruce.roflcraft.rpg;

public class Skill 
{
	private String name;
	private int value;
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String skillName)
	{
		name = skillName;
	}
	
	public int getValue()
	{
		return value;
	}
	
	public void setValue(int skillValue)
	{
		value = skillValue;
	}
}
