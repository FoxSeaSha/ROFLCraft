package bruce.roflcraft.rpg;

public class Attribute 
{
	private String name;
	private int value;
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String attributeName)
	{
		name = attributeName;
	}
	
	public int getValue()
	{
		return value;
	}
	
	public void setValue(int attributeValue)
	{
		value = attributeValue;
	}
}
